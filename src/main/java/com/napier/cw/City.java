package com.napier.cw;

/**
 * Represents an City
 */
public class City
{
    /**
     * City int;
     */
    public int cityid;

    /**
     * City's Name
     */
    public String cname;

    /**
     * City's Country Code
     */
    public String cccode;

    /**
     * City's District
     */
    public String cd;

    /**
     * City's Population
     */
    public int cpop;
}